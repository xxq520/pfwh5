<p align="center" style="text-align:center;text-indent:21.0pt;background:white;">
	<p class="MsoNormal" align="center" style="text-align:center;">
		<b>&nbsp;<span>520</span>批发网业务代理商协议<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>&nbsp;&nbsp;&nbsp; </b>鉴于<span>:</span>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		甲方广州华项科技有限公司为<span>520</span>批发网软件的拥有与运营方（以下简称甲方）<span></span>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		乙方因需要拓展当地批发商（包括产品厂商、供应商、批发商等）入驻并使用软件技术系统开展销售业务与在线批发与服务，从中获得业务佣金，因此与甲方缔结本协议。<span></span>
	</p>
	<p class="MsoBodyText" style="text-indent:26.25pt;">
		根据《中华人民共和国合同法》等相关法律规定，甲、乙双方本着真诚合作、互利互惠的原则，就乙方代理“<span>520</span>批发网”软件业务合作的相关事宜达成以下协议，以希共同遵守：<span></span>
	</p>
	<p class="MsoNormal" style="margin-left:6.0pt;">
		<b>一、合作模式 </b>&nbsp;&nbsp;
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		甲方为“<span>520</span>批发网”的软件开发与拥有方，软件上需要众多合格的产品厂商、供应商、批发商入驻使用“<span>520</span>批发网”软件系统<span>,</span>以服务更多的商家。乙方在缴纳协议保证金后方可申请成为甲方的业务代理商，拓展合格的产品厂商、供应商、批发商<span>(</span>批发商类型属于已是甲方的代理（伙伴）、线下服务中心的除外，下同<span>)</span>入驻并付费使用甲方软件系统功能，乙方从中获得相应的业务佣金。<u><span></span></u>
	</p>
	<p class="MsoBodyText" style="text-indent:10.55pt;">
		<b>二、 代理期限和代理区域<span></span></b>
	</p>
	<p class="MsoBodyText" style="text-indent:10.5pt;">
		1、<b>代理期限：<u>壹</u>年，自乙方点击“确认”本协议之日起生效。</b>本协议到期前<span>30</span>天，乙方可根据实际情况向甲方提出续约申请，双方达成一致后可续签协议。如任意一方均未提出申请的，本协议到期后自动终止。<span>&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>
	</p>
	<p class="MsoBodyText" style="text-indent:15.75pt;">
		2、乙方业务代理区域按下述第三条“保证金”之第<span>1</span>款约定，以交付的保证金金额和乙方自行填写的常在与经营地址确定其代理区域（具体以双方签订的纸制协议为准），乙方在代理区域内依本协议享有代理权及相关权益。本代理权非独家代理权，甲方仍可以与其他代理签订合作协议，授权其他人在该区域代理，开展业务，但不影响乙方在协议期内的代理权。<span></span>
	</p>
	<p class="MsoBodyText" style="text-indent:10.55pt;">
		<b>三、 保证金<span></span></b>
	</p>
	<p class="MsoBodyText" style="text-indent:15.8pt;">
		<b>1</b><b>、乙方应在本协议签订之日根据其业务代理区域范围向甲方缴纳合作合同保证金，并付款至甲方指定账户，保证金不计算利息，具体明细如下，由乙方自行选择，以甲方本协议指定帐户收到的实际保证金金额为准，甲方并以此确定乙方的代理权区域（具体以双方签订的纸制协议为准）。<span></span></b>
	</p>
	<p class="MsoBodyText" style="text-indent:15.75pt;">
		( 注<span>:</span>业务代理行政区域为一个省级范围的，保证金为壹万元；业务代理行政区域为一个市级范围的，保证金为伍仟元；业务代理行政区域为一个县级范围的，保证金为叁仟元。<span>)</span>
	</p>
	<p class="MsoBodyText" style="text-indent:21.0pt;">
		2、因乙方原因造成甲方任何损失的，甲方有权优先通过扣除乙方保证金或从应付佣金中进行扣抵，不足扣抵的，乙方仍应就不足部分予以赔偿甲方。<span></span>
	</p>
	<p class="MsoBodyText" style="text-indent:15.75pt;">
		3、甲方根据本协议约定受理乙方退回保证金申请且在收到乙方退回协议、收据等原件后三个月内由甲方一次性无息退还乙方向甲方所缴纳的保证金余额。<span></span>
	</p>
	<p class="MsoNormal">
		<b>四、 乙方的权利义务<span></span></b>
	</p>
	<p class="MsoNormal" style="text-indent:21.1pt;">
		<b>1</b><b>、乙方成为本协议约定的业务代理商后，乙方拓展的批发商（含产品厂商、供应商、批发贸易商，以下同）以“年度服务费”的方式向甲方支付软件服务费的，则乙方拓展的批发商实际支付给甲方的年度服务费的<span>60%</span>为乙方的业务佣金；乙方拓展的批发商（含产品厂商、供应商、批发贸易商，以下同）以“扣点”的方式向甲方支付软件服务费。具体操作方式为：甲方系统默认扣取乙方拓展的批发商在“<span>520</span>批发网”软件上与其下游商家发生的交易额的<span>2%</span>（不低于<span>2%</span>）作为软件服务费。其中<span>1%</span>为甲方收取的软件服务费，余下<span>1%</span>为乙方的业务佣金。乙方也可以凭自己的业务能力与服务付出适当提高批发商使用<span>520</span>批发网软件上的服务费扣点，但乙方与批发商约定的交易扣点不得高于<span>15%</span>，并须委托甲方代收。甲方依约均只收取<span>1%</span>的软件服务费，余下的均为乙方的业务佣金。乙方的业务佣金所产生的所有税赋费用均须由乙方自己依法承担，甲方不对交税负责。乙方开发业务佣金的产生以乙方拓展（经由系统推荐发生）的批发商经甲方认可、实际使用甲方软件后产生的交易扣点或年度服务费、以及甲方为乙方拓展的批发商正式开通账号为基础<span>(</span>批发商类型如是本属于甲方的代理（伙伴）、线下服务中心的则不产生佣金<span>)</span>。乙方的佣金由甲方依据国家法律法规规定代扣代缴个人所得税后且乙方拓展的批发商依据协议履行完应尽的责任和义务后的次月，经乙方在系统中自行提现，甲方向乙方支付应得佣金。<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>注：甲方有权根据实际情况调整乙方的佣金比例，以甲方的公示或通知为准。<span></span></b>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		2、 乙方应不定时检查和督促自己拓展的批发商依据协议约定和国家有关法律法规的规定供应合法合格且合适的商品，以保证各方的良好合作。<span></span>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		3、 乙方每周应至少现场检查其拓展的批发商一次，出具图文并具的书面报告上传给甲方审验，并对报告内容的真实性负责。如乙方发现批发商异常，应及时通知甲方关停其功能、处理其异常。<span></span>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		4、乙方应保持与其拓展的批发商的日常沟通与联系，掌握批发商的实际情况与动态，督促其为业务客户及商家提供优质诚信服务，确保批发商的正常供货和守法经营，且不出现任何产品与服务质量问题。<span></span>
	</p>
	<p class="MsoNormal" style="text-indent:21.0pt;">
		5、无论批发商是否存在显性异常情况，如甲方提出要求，乙方应即时亲临批发商现场处理与了解问题，尽早妥善解决并将事态控制在可控状态下。<span></span>
	</p>
	<p class="MsoBodyText" style="text-indent:21.0pt;">
		6、乙方应严格遵守相关法律法规及甲方规定的各项管理制度，并遵守诚实信用原则。<span></span>
	</p>
	<p class="MsoNormal">
		<b>五、 违约责任<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>（一）如有以下行为，甲方有权随时终止与乙方的所有合作，视情况要求乙方退还甲方已付或扣取未付的佣金，并要求乙方赔偿包括调查取证费、诉讼费、律师费在内的甲方相应损失：<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>1、 </b><b>乙方拓展的批发商在<span>520</span>批发网软件上的供货价（含批发价、零售价）高于甲方其它直接或潜在竞争对手（或该批发商的其它客户），损害甲方利益和名誉，乙方知道或应当知道而不向甲方报告的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>2、 </b><b>乙方拓展的批发商违反国家法律、法规的强制性规定，利用甲方软件从事非法活动，乙方知道或应当知道而不向甲方报告的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>3、 </b><b>乙方拓展的批发商违背诚信原则、弄虚作假，严重损害甲方以及<span>520</span>批发网软件或其他业务客户的声誉，乙方知道或应当知道而不向甲方报告的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>4、 </b><b>乙方拓展的批发商所供货的商品出现重大质量或货期延误问题，乙方不及时协助甲方处理，包庇该批发商的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>5、 </b><b>监督、管理批发商失责，造成甲方经济损失，属于乙方不作为的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>6、 </b><b>乙方拓展的批发商行贿甲方工作人员，乙方知情不报的，或乙方向批发商索贿的（贿赂为协议约定外的任何其他利益，包括但不限于明扣、暗扣、现金、购物卡、实物、有价证券、旅游或其他非物质性利益等）或乙方接受批发商的业务宴请，接受批发商的礼品礼金，不属于正常人情往来，经查属实的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>7、 </b><b>乙方或其拓展的批发商与甲方的商家或客户绕开甲方私下交易，直接发生业务往来，损害甲方利益的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>8、 </b><b>乙方未尽到保密义务，导致协议内容被甲乙双方之外的第三方所知悉的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>9、 </b><b>违背诚信原则、弄虚作假，严重损害甲方以及甲方关联公司的声誉；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:32.2pt;text-indent:-18.0pt;">
		<b>10、 </b><b>损害甲方或甲方软件利益的其他情形。<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>（二） 如有以下行为，本协议终止：<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:39.25pt;text-indent:-18.0pt;">
		<b>1、 </b><b>乙方拓展的批发商事实上已终止正常运作或营业执照已被吊销或注销；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:39.25pt;text-indent:-18.0pt;">
		<b>2、 </b><b>乙方与其拓展的批发商的正常合作关系已经终止，无法正常履行监管职责的；<span></span></b>
	</p>
	<p class="MsoNormal" style="margin-left:39.25pt;text-indent:-18.0pt;">
		<b>3、 </b><b>乙方书面申请终止协议并经甲方同意的。<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>六、</b>520批发网（含<span>520</span>生活圈）软件现在及将来发布的全部规则、公告、说明等，都是本协议不可分割的部分，与本协议具有同等法律效力。<span></span>
	</p>
	<p class="MsoNormal">
		<b>七、甲、乙双方应严格履行本协议的各项条款，发生争议，双方应当友好协商解决，协商不成，可向广州仲裁委员会进行仲裁解决。<span></span></b>
	</p>
	<p class="MsoNormal">
		<b>八、特别约定条款：<span></span></b>
	</p>
	<p class="MsoNormal" style="text-indent:10.5pt;">
		1、乙方须保证拓展的批发商在<span>520</span>批发网软件批发给其客户的供货价在同比条件下是最低价格，否则，甲方有权单方解除本协议；<span></span>
	</p>
	<p class="MsoNormal" align="left" style="text-align:left;text-indent:10.5pt;">
		2、乙方拓展的批发商如为大型集团公司其中的下属公司，则该集团公司下属的其它下属公司可自行与甲方或甲方其他合作者签订合作协议，而与乙方无关。<span></span>
	</p>
	<p class="MsoNormal" align="left" style="text-align:left;text-indent:10.5pt;">
		3、本协议所指“批发商”既包括传统各行各业的批发网，也包括各行各业的生产厂商，但不包括甲方的代理（伙伴）、线下服务中心类型的批发商。<span></span>
	</p>
	<p class="MsoNormal" align="left" style="text-align:left;text-indent:10.5pt;">
		4、乙方拓展不止一家批发商入驻甲方<span>520</span>批发网使用软件功能的，亦适用本协议相关条款。<span></span>
	</p>
	<p class="MsoNormal" align="left" style="text-align:left;text-indent:10.5pt;">
		5、甲方是软件服务提供方，不从事商品的贸易流通，除收取协议约定的必要的软件服务费外不在任何一方的商品或服务买卖中获益，只按交易额收取软件服务费，所以，只对软件服务质量负责，而不对乙方、乙方拓展的批发商及其商户的合作、交易结果负责，各方须自行依法承担各自法律责任。<span></span>
	</p>
	<p class="MsoNormal" align="left" style="text-align:left;">
		&nbsp; 6、各方均清楚明白甲方的原则立场和本协议约定的事实，各方对此无争议。<span></span>
	</p>
